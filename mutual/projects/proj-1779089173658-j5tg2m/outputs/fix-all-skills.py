import os, json, shutil, glob, re

SKILLS = r'C:\Users\13975\.newmax\skills'

def read_file(path):
    try:
        with open(path, 'r', encoding='utf-8') as f:
            return f.read()
    except:
        return ''

def write_file(path, content):
    os.makedirs(os.path.dirname(path), exist_ok=True)
    with open(path, 'w', encoding='utf-8') as f:
        f.write(content)

def append_to_skill(skill_name, section_content):
    """在 SKILL.md 末尾追加内容段"""
    path = os.path.join(SKILLS, skill_name, 'SKILL.md')
    content = read_file(path)
    if not content:
        return
    # 检查是否已有该 section
    first_line = section_content.split('\n')[0].strip()
    if first_line and first_line.lstrip('#').strip() in content:
        return  # 已有，跳过
    content = content.rstrip() + '\n\n' + section_content
    write_file(path, content)


def fix_domain(filepath, num):
    """修正单个文件的 domain 字段"""
    if num not in CORRECT_DOMAINS:
        return False

    code, name = CORRECT_DOMAINS[num]

    with open(filepath, 'r', encoding='utf-8') as f:
        content = f.read()

    if not content.startswith('---'):
        return False

    # 替换 domain 行
    new_content = re.sub(
        r'domain:\s*"[^"]*"',
        f'domain: "{code}"',
        content,
        count=1
    )

    if new_content != content:
        with open(filepath, 'w', encoding='utf-8') as f:
            f.write(new_content)
        return True
    return False

def main():
    files = glob.glob(os.path.join(VAULT, '*.md'))
    fixed = 0

    for fp in sorted(files):
        fn = os.path.basename(fp)
        m = re.match(r'^(\d{3})-', fn)
        if not m:
            continue
        num = int(m.group(1))
        if num == 0:
            continue  # 跳过 000-总索引

        if fix_domain(fp, num):
            code, name = CORRECT_DOMAINS[num]
            print(f"  # {fn[:50]:50s} → {code} {name}")
            fixed += 1

    print(f"\n修正了 {fixed} 个文件的域分类")

if __name__ == '__main__':
    main()
